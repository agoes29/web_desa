        $validatedData = $request->validate([
            'image' => 'required|image',

        ]);
        $file = $request->file('image');
        $name = $file->getClientOriginalName();
        $name = str_replace(' ', '_', $name);
        $path = $file->storeAs('public/images', $name);

        $image = $path;
        $home = new Blog();
        $home->image = $image;
        $home->save();
        return redirect('/');
